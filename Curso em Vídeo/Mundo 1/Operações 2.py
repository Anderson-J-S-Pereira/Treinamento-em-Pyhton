n1 = float(input('Número 1: '))
n2 = float(input('Número 2: '))
som = n1 + n2
sub = n1 - n2
mul = n1 * n2
div = n1 / n2
print('A soma desse cálculo é: {:.1f}. A subtração é: {:.2f}. A multiplicação é: {:.3f}. E a divisão é: {:.4f}'.format(som, sub, mul, div))
